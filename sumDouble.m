function y = sumDouble(a,b)
% sumDouble(a,b) - returns integer value as a result 
% if a is not equal to b then the result is the sum of the two numbers
% otherwise if a is equal to b, the result is twice the sum of the two numbers
% Author: Mehreen Asad
% Homework # 4
% Due date: 2/17/2011

if (a~= b)
     y = a+b;

else
     y = 2*(a+b);
end

